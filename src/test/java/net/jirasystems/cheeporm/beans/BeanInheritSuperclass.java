package net.jirasystems.cheeporm.beans;

import javax.persistence.Column;
import javax.persistence.Id;

public class BeanInheritSuperclass {

	@Id
	@Column
	private Integer id;

	@Column
	private String name;

	/**
	 * @return the id
	 */
	public Integer getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
